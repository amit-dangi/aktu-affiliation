package com.sits.affiliation.approval.inspection_upload_doc;

public class UploadDocModel {
	private String doc_id;
	private String doc_name;
	private String doc_compul;
	private String attid;
	private String filename;
	private String mid;
	private String type;
	
	public String getDoc_id() {
		return doc_id;
	}
	public void setDoc_id(String doc_id) {
		this.doc_id = doc_id;
	}
	public String getDoc_name() {
		return doc_name;
	}
	public void setDoc_name(String doc_name) {
		this.doc_name = doc_name;
	}
	public String getDoc_compul() {
		return doc_compul;
	}
	public void setDoc_compul(String doc_compul) {
		this.doc_compul = doc_compul;
	}
	public String getAttid() {
		return attid;
	}
	public void setAttid(String attid) {
		this.attid = attid;
	}
	public String getFilename() {
		return filename;
	}
	public void setFilename(String filename) {
		this.filename = filename;
	}
	public String getMid() {
		return mid;
	}
	public void setMid(String mid) {
		this.mid = mid;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	
	
}
